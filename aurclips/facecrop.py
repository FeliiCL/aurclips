"""Detección de rostros para encuadre vertical inteligente (9:16).

Al pasar un clip a formato vertical hay que recortar los lados. Recortar
siempre por el centro geométrico funciona mal cuando la persona que habla
está descentrada: se le corta la cara. Este módulo muestrea unos fotogramas
del tramo del clip, detecta el rostro dominante y devuelve la expresión de
recorte de ffmpeg centrada en él; si no hay rostro, cae al recorte central.

Detector primario: el clasificador Haar que incluye OpenCV
(``cv2.CascadeClassifier``), tal como es habitual. Algunas builds recientes
(p. ej. opencv-python-headless 5.x) ya no traen el módulo ``objdetect`` ni
los ``.xml`` de Haar; en ese caso se recurre automáticamente al detector DNN
YuNet (``cv2.FaceDetectorYN``), cuyo modelo va embebido y comprimido al final
de este archivo para no depender de la red durante el render.
"""

from __future__ import annotations

import base64
import gzip
import json
import os
import statistics
import subprocess
import tempfile

# fotogramas que se muestrean a lo largo del tramo del clip
_NUM_MUESTRAS = 7
# ancho al que se reducen los fotogramas antes de detectar (por velocidad)
_ANCHO_ANALISIS = 480
# umbral de confianza del detector DNN (YuNet)
_YUNET_SCORE = 0.6
# recorte central simbólico: idéntico al que usa render.py por defecto
_CROP_CENTRADO = "crop=ih*9/16:ih"


def face_crop_filter(cfg, video_path: str, start: float, end: float) -> str:
    """Devuelve la expresión de filtro ffmpeg de recorte 9:16 para el clip.

    Si detecta rostro(s): centra el recorte horizontal en el rostro dominante.
    Si no: devuelve el recorte centrado "crop=ih*9/16:ih".
    """
    try:
        import cv2  # import perezoso: no penalizar el arranque del CLI

        # silencia los avisos internos de la DNN para no ensuciar el log
        try:
            cv2.utils.logging.setLogLevel(cv2.utils.logging.LOG_LEVEL_SILENT)
        except Exception:
            pass

        w, h = _dimensiones(cfg, video_path)
        if not w or not h:
            print("  [crop] sin rostro, recorte centrado")
            return _CROP_CENTRADO

        # ancho del recorte 9:16 (par). Si el vídeo ya es más estrecho que 9:16
        # no hay nada que recortar horizontalmente -> recorte central.
        crop_w = _par(h * 9 / 16)
        if crop_w >= w:
            print("  [crop] sin rostro, recorte centrado")
            return _CROP_CENTRADO

        centros = _centros_rostros(cfg, cv2, video_path, start, end, w)
        # la mediana simple de los centros x es robusta frente a detecciones
        # sueltas erróneas; se exige rostro en al menos 2 fotogramas
        if len(centros) < 2:
            print("  [crop] sin rostro, recorte centrado")
            return _CROP_CENTRADO

        centro_x = statistics.median(centros)
        x = int(round(centro_x - crop_w / 2))
        x = max(0, min(x, w - crop_w))
        x -= x % 2  # a entero par (hacia abajo; sigue dentro del rango válido)
        print(f"  [crop] rostro detectado en x={int(round(centro_x))} "
              f"({len(centros)}/{_NUM_MUESTRAS} fotogramas) -> recorte x={x}")
        return f"crop={crop_w}:{h}:{x}:0"
    except Exception:
        # nunca romper el render: ante cualquier fallo interno, recorte central
        print("  [crop] sin rostro, recorte centrado")
        return _CROP_CENTRADO


# ---------------------------------------------------------------------------
# Dimensiones y muestreo de fotogramas
# ---------------------------------------------------------------------------

def _dimensiones(cfg, video_path: str):
    """Ancho y alto reales del vídeo (stream de vídeo v:0), vía ffprobe."""
    r = subprocess.run(
        [cfg.ffprobe, "-v", "error", "-select_streams", "v:0",
         "-show_entries", "stream=width,height", "-of", "json", video_path],
        capture_output=True, text=True, encoding="utf-8", errors="replace",
    )
    if r.returncode != 0:
        return None, None
    try:
        st = json.loads(r.stdout)["streams"][0]
        return int(st["width"]), int(st["height"])
    except (KeyError, IndexError, ValueError, json.JSONDecodeError):
        return None, None


def _centros_rostros(cfg, cv2, video_path: str, start: float, end: float,
                     w: int) -> list[float]:
    """Centros x (en coordenadas del vídeo original) del rostro dominante de
    cada fotograma muestreado donde se detectó una cara."""
    detector = _crear_detector(cv2)
    if detector is None:
        return []

    # se reduce el ancho para acelerar; el centro detectado se reescala luego
    tw = w if w <= _ANCHO_ANALISIS else _ANCHO_ANALISIS
    escala = tw / w  # cx_original = cx_reducido / escala
    dur = max(0.0, end - start)

    centros: list[float] = []
    for i in range(_NUM_MUESTRAS):
        # puntos centrados en _NUM_MUESTRAS sub-intervalos iguales; así se
        # evita muestrear justo en los extremos (posibles negros / fin de EOF)
        t = start + (i + 0.5) / _NUM_MUESTRAS * dur if dur > 0 else start
        img = _extraer_frame(cfg, cv2, video_path, t, tw)
        if img is None:
            continue
        caja = _rostro_mayor(detector, img)  # el rostro de mayor área
        if caja is None:
            continue
        x, _y, bw, _bh = caja
        centros.append((x + bw / 2) / escala)
    return centros


def _extraer_frame(cfg, cv2, video_path: str, t: float, tw: int):
    """Extrae un fotograma en el instante absoluto ``t`` del archivo fuente,
    reducido a ``tw`` px de ancho, y lo devuelve como imagen BGR (o None)."""
    import numpy as np

    r = subprocess.run(
        [cfg.ffmpeg, "-nostdin", "-loglevel", "error",
         "-ss", f"{max(0.0, t):.3f}", "-i", video_path,
         "-frames:v", "1", "-vf", f"scale={tw}:-2",
         "-f", "image2pipe", "-vcodec", "mjpeg", "-"],
        capture_output=True,
    )
    if r.returncode != 0 or not r.stdout:
        return None
    buf = np.frombuffer(r.stdout, dtype=np.uint8)
    return cv2.imdecode(buf, cv2.IMREAD_COLOR)


def _rostro_mayor(detector, img):
    """Devuelve la caja (x, y, w, h) del rostro de mayor área, o None."""
    cajas = detector(img)
    if not cajas:
        return None
    return max(cajas, key=lambda b: b[2] * b[3])


# ---------------------------------------------------------------------------
# Detectores de rostro (Haar primario, YuNet de respaldo)
# ---------------------------------------------------------------------------

def _crear_detector(cv2):
    """Elige el mejor detector disponible. Devuelve un callable
    ``det(img_bgr) -> [(x, y, w, h), ...]`` o None si no hay ninguno."""
    # 1) Primario: clasificador Haar incluido en OpenCV
    if hasattr(cv2, "CascadeClassifier"):
        try:
            ruta = cv2.data.haarcascades + "haarcascade_frontalface_default.xml"
            if os.path.exists(ruta):
                clf = cv2.CascadeClassifier(ruta)
                if not clf.empty():
                    return _DetectorHaar(cv2, clf)
        except Exception:
            pass
    # 2) Respaldo: detector DNN YuNet con el modelo embebido. Necesario en
    #    builds sin objdetect/Haar (p. ej. opencv-python-headless 5.x).
    if hasattr(cv2, "FaceDetectorYN"):
        try:
            return _DetectorYuNet(cv2, _ruta_modelo_yunet())
        except Exception:
            pass
    return None


class _DetectorHaar:
    """Clasificador Haar frontal de OpenCV (cv2.CascadeClassifier)."""

    def __init__(self, cv2, clf):
        self._cv2 = cv2
        self._clf = clf

    def __call__(self, img):
        cv2 = self._cv2
        gris = cv2.cvtColor(img, cv2.COLOR_BGR2GRAY)
        h, w = gris.shape[:2]
        lado_min = max(20, int(min(h, w) * 0.12))  # minSize proporcional
        caras = self._clf.detectMultiScale(
            gris, scaleFactor=1.1, minNeighbors=5,
            minSize=(lado_min, lado_min),
        )
        return [(int(a), int(b), int(c), int(d)) for (a, b, c, d) in caras]


class _DetectorYuNet:
    """Detector DNN YuNet de OpenCV (cv2.FaceDetectorYN)."""

    def __init__(self, cv2, modelo):
        self._det = cv2.FaceDetectorYN.create(
            modelo, "", (320, 320), _YUNET_SCORE, 0.3, 5000,
        )

    def __call__(self, img):
        h, w = img.shape[:2]
        self._det.setInputSize((w, h))
        _, caras = self._det.detect(img)
        if caras is None:
            return []
        # cada fila: [x, y, w, h, 5 landmarks (10 valores), score]
        return [(int(f[0]), int(f[1]), int(f[2]), int(f[3])) for f in caras]


def _ruta_modelo_yunet() -> str:
    """Materializa el modelo YuNet embebido en un archivo de caché (una sola
    vez) y devuelve su ruta."""
    ruta = os.path.join(tempfile.gettempdir(), "aurclips_yunet_face_int8.onnx")
    if not (os.path.exists(ruta) and os.path.getsize(ruta) > 0):
        datos = gzip.decompress(base64.b64decode("".join(_MODELO_YUNET_B64)))
        tmp = f"{ruta}.{os.getpid()}.tmp"
        with open(tmp, "wb") as f:
            f.write(datos)
        os.replace(tmp, ruta)  # publicación atómica (evita lecturas parciales)
    return ruta


def _par(n) -> int:
    """Redondea al entero par más cercano por debajo (ffmpeg exige medidas
    pares para yuv420p)."""
    n = int(round(n))
    return n - (n % 2)


